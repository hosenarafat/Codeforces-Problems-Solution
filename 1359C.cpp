double tmpr, h, c;
double avarg(double n)
{
    double tmp= (n*h + (n-1)*c )/(2*n-1) ;
    return tmp;
}

int main()
{
    ll t;
    while(t--)
    {
        double ans=0 ;
        cin>>h>>c>>tmpr;

        double l=1, r=inf, tmp;

        double avg=(h+c)/2;

        if(avg == tmp){cout<<2<<endl; return 0;  }

        while(l<=r)
        {
            double mid=(l+r)/2;
            avg=avarg(mid);

            if(avg==tmpr){ans=mid; break;}

            if(avg>=tmpr)
            {
                l=mid+1;
                ans=mid;
            }
            else  r=mid-1 ;
        }

        cout<<ans<<endl;
    }
return 0;
}

